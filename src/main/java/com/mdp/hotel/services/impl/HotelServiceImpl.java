package com.mdp.hotel.services.impl;

import com.mdp.hotel.entities.Hotel;
import com.mdp.hotel.exception.ResourceNotFoundException;
import com.mdp.hotel.repositories.HotelRepository;
import com.mdp.hotel.services.HotelService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

import java.util.List;
import java.util.UUID;

@Service
public class HotelServiceImpl implements HotelService {

    @Autowired
    private HotelRepository hotelRepository;

    @Override
    public Hotel createHotel(Hotel hotel) {
        String hotelId= UUID.randomUUID().toString();
        hotel.setId(hotelId);
        return hotelRepository.save(hotel);
    }

    @Override
    public List<Hotel> getAll() {
        return hotelRepository.findAll();
    }

    @Override
    public Hotel getHotel(String id) {
        return hotelRepository.findById(id).orElseThrow(()->  new ResourceNotFoundException("Given hotel not found"));
    }
}
